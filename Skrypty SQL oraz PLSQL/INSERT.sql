CREATE OR REPLACE VIEW KLIENT_FAKTURA AS
  SELECT COUNT(FAKTURA.ID) AS ILOSC_FAKTUR,KLIENT.NAZWA,KLIENT.NIP FROM FAKTURA, KLIENT
  WHERE KLIENT.ID=FAKTURA.KLIENT_ID GROUP BY KLIENT.NAZWA,KLIENT.NIP
  WITH CHECK OPTION;
  
